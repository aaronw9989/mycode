#!/usr/bin/env python3

def main():

    # Get the name of the user
    user_name = input("Please enter a user name: ")

    # Get the day of the week
    day_of_week = input("Please enter what day of the week it is? ") 

    # Output the user name and day of the week
    print(f"Hello, {user_name}! Happy {day_of_week}!")


# call the main function
if __name__ == "__main__":
    main()
